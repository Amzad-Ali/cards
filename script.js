const option1 = document.getElementById('option1');

option1.addEventListener('mouseover', () => {
    const drop1 = document.getElementById('dropdown1');
    const most = document.getElementById('most-popular1');

    drop1.style.display = drop1.style.display === "none" ? "block" : "none";

    // Toggle visibility of most popular section
    most.style.display = most.style.display === "none" ? "block" : "none";
})

option1.addEventListener('mouseout', () => {
    const drop1 = document.getElementById('dropdown1');
    const most = document.getElementById('most-popular1');

    drop1.style.display = drop1.style.display === "none" ? "block" : "none";

    // Toggle visibility of most popular section
    most.style.display = most.style.display === "none" ? "block" : "none";
})

